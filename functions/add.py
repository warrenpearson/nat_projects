def add(a, b):
    c = a + b
    return c

def minus(a, b):
    c = a - b
    return c

def divide(a, b):
    c = a / b
    return c

def multiply(a, b):
    c = a * b
    return c

ans_1 = add(2, 3)
print(ans_1)

ans_2 = add(4, 3)
print(ans_2)

ans_3 = minus(5, 1)
print(ans_3)

ans_4 = divide(9, 3)
print(ans_4)

ans_5 = multiply(3, 3)
print(ans_5)
